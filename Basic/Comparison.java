package DataStoredandOperators;

public class Comparison {

	public static void main(String[] args) {
		int a = 3;
		int b = 7;
		System.out.println(a > b);//it will return boolean value
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a < b);
		System.out.println(a >= b);
	}
}
